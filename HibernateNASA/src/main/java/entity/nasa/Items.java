package entity.nasa;


public class Items {
	
	private String href;
	private Data[] data;
	private Links[] links;
	
	
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public Data[] getData() {
		return data;
	}
	public void setData(Data[] data) {
		this.data = data;
	}
	public Links[] getLinks() {
		return links;
	}
	public void setLinks(Links[] links) {
		this.links = links;
	}
	

}
